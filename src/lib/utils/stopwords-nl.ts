/**
 * Check of een woord een stopwoord is (case-insensitive).
 * Strips leestekens van begin en eind.
 */
export function isStopword(word: string): boolean {
	const cleaned = word.toLowerCase().replace(/^[^a-zà-ÿ]+|[^a-zà-ÿ]+$/g, '');
	return STOPWORDS_NL.has(cleaned);
}
